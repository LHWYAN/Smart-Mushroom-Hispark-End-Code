#ifndef __BUZZER_H__
#define __BUZZER_H__

#include <stdint.h>
#include "common_def.h"

/* 可根据实际硬件连接修改蜂鸣器的GPIO引脚号，默认使用GPIO_09（pin = 9） */
#ifndef BUZZER_GPIO_PIN
#define BUZZER_GPIO_PIN GPIO_09
#endif

/**
 * @brief 蜂鸣器初始化
 * @return int 成功返回0
 * 
 * 功能：初始化蜂鸣器GPIO配置
 *      设置引脚复用模式为普通IO、方向为输出、初始电平为高（关闭）
 */
int Buzzer_Init(void);

/**
 * @brief 蜂鸣器打开 (低电平触发)
 * 
 * 功能：将GPIO输出低电平，蜂鸣器发声
 */
void Buzzer_On(void);

/**
 * @brief 蜂鸣器关闭 (高电平)
 * 
 * 功能：将GPIO输出高电平，蜂鸣器停止发声
 */
void Buzzer_Off(void);

/**
 * @brief 蜂鸣器状态翻转
 * 
 * 功能：翻转GPIO输出电平，实现蜂鸣器状态切换
 */
void Buzzer_Toggle(void);

#endif /* __BUZZER_H__ */
